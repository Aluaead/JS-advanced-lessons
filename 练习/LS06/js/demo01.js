//逻辑与、或的基本理解
console.log("demo1");
console.log("--------------------");

console.log(2 > 1 && 4 < 5);
console.log(true && (!2));
console.log(false && ("2" == 2));
console.log(false && false);
console.log("--------------------");

console.log(2 > 1 || 4 < 5);
console.log(true || (!2));
console.log(false || ("2" == 2));
console.log(false || false);
console.log("--------------------");